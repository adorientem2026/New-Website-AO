function setText(selector, value) {
  document.querySelectorAll(selector).forEach((element) => {
    element.textContent = value;
  });
}

function applyLanguage(language) {
  const dictionary = translations[language] || translations.en;

  document.documentElement.lang = language;
  document.title = dictionary.pageTitle;

  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription) {
    metaDescription.setAttribute('content', dictionary.metaDescription);
  }

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.getAttribute('data-i18n');
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll('[data-book]').forEach((element) => {
    const key = element.getAttribute('data-book');
    if (dictionary.books[key]) {
      element.textContent = dictionary.books[key];
    }
  });

  document.querySelectorAll('[data-alt-book]').forEach((image) => {
    const key = image.getAttribute('data-alt-book');
    if (dictionary.books[key]) {
      image.setAttribute('alt', `${dictionary.coverPrefix} ${dictionary.books[key]}`);
    }
  });

  document.querySelectorAll('.language-button').forEach((button) => {
    const isActive = button.getAttribute('data-lang') === language;
    button.classList.toggle('is-active', isActive);
    button.setAttribute('aria-pressed', String(isActive));
  });

  try {
    localStorage.setItem('adOrientemLanguage', language);
  } catch (error) {
    // localStorage can be unavailable in strict privacy modes; the site still works.
  }
}

function getInitialLanguage() {
  try {
    const storedLanguage = localStorage.getItem('adOrientemLanguage');
    if (storedLanguage && translations[storedLanguage]) {
      return storedLanguage;
    }
  } catch (error) {
    // Ignore and fall back to English.
  }

  const browserLanguage = navigator.language || navigator.userLanguage || 'en';
  return browserLanguage.toLowerCase().startsWith('fr') ? 'fr' : 'en';
}

document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('.language-button').forEach((button) => {
    button.addEventListener('click', () => {
      applyLanguage(button.getAttribute('data-lang'));
    });
  });

  applyLanguage(getInitialLanguage());
});
